package me.qeklydev.scoreboard.type;

/**
 * This enum is used to represent the current toggled
 * status for each scoreboard.
 *
 * @since 0.0.1
 */
public enum ScoreboardToggleStateType {
  /**
   * The scoreboard is visible for the player.
   *
   * @since 0.0.1
   */
  VISIBLE,
  /**
   * The scoreboard is closed for visibility.
   *
   * @since 0.0.1
   */
  CLOSED
}
